#pragma once

// =============================================================
// [핀 설정]
// RGB LED 각 색상 채널에 연결된 ESP32-C3 GPIO 핀 번호
// =============================================================
#define R_PIN  2   // 빨간색 LED 핀 (Red)
#define G_PIN  3   // 초록색 LED 핀 (Green)
#define B_PIN  4   // 파란색 LED 핀 (Blue)

// =============================================================
// [PWM 설정]
// PWM(펄스 폭 변조)으로 LED 밝기를 조절합니다.
// - PWM_FREQ  : 주파수(Hz). 5000Hz = 사람 눈에 깜빡임 안 보임
// - PWM_RES   : 해상도(비트). 8비트 = 0~255 단계
// - CHANNEL_R/G/B : ESP32 LEDC 채널 번호 (0~7 중 선택)
// =============================================================
#define PWM_FREQ    5000   // PWM 주파수 (Hz)
#define PWM_RES     8      // PWM 해상도 (비트, 8 = 0~255)

#define CHANNEL_R   0      // 빨간색 LEDC 채널
#define CHANNEL_G   1      // 초록색 LEDC 채널
#define CHANNEL_B   2      // 파란색 LEDC 채널

// =============================================================
// [타이밍 설정]
// =============================================================
#define COLOR_HOLD_MS  1000UL   // 한 색상 유지 시간 (밀리초)
#define BAUD_RATE      115200   // 시리얼 통신 속도
